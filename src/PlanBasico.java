public class PlanBasico {
    protected int numero;
    protected String nombre;
    
    
    public PlanBasico(int numero, String nombre){
        this.numero = numero;
        this.nombre = nombre;
    }

    public int getNumero() {
        return numero;
    }

    public String getNombre() {
        return nombre;
    }
    
    
    
}
